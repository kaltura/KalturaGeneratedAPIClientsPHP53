	 */
	public $systemNameIn = null;

	/**
	 * 
	 * @var string
	 */
	public $tagsMultiLikeOr = null;

	/**
	 * 
	 * @var string
	 */
	public $tagsMultiLikeAnd = null;

	/**
	 * 
	 * @var string
	 */
	public $defaultEntryIdEqual = null;

	/**
	 * 
	 * @var string
	 */
	public $defaultEntryIdIn = null;

}
